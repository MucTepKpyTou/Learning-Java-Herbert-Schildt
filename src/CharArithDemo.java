public class CharArithDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("сh содержит " + ch);
        ch++; //инкремент переменной +1
        System.out.println("Теперь ch содержит " + ch);
        ch = 90; // присвоить переменной значение Z
        System.out.println("Теперь перменная ch содержит " + ch);
    }
}
